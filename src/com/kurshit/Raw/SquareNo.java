package com.kurshit.Raw;

public class SquareNo {

	public static void main(String[] args) {
		
		double p=15;
		double r=11;		
		double n=7;
		myIntrest(p,r,n);
	
		double l=7;
		double m=8;
		double s=10;
		myIntrest(l,m,s);
	}
	
	static void myIntrest(double p,double r,double n)
	{
		double si;
		si=(p*r*n)/100;
		System.out.println(si);		
	}
	
  } 
	
	
	